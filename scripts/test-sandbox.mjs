  console.log("");
  console.log("Test sandbox is ready (isolated from .data/pglite and staging).");
  console.log("");
  console.log("Demo accounts:");
  console.log("  Owner  — phone 9876543210  (Demo Owner)");
  console.log("  Broker — phone 9876543211  (Demo Broker)");
  console.log("");
  console.log("Data path:", sandboxPglite);
  console.log("Env file:", sandboxEnvPath);
  console.log("");
  console.log("Start dev stack:  pnpm run test:db:dev");
  console.log("Seed onboard link: pnpm run test:db:seed-onboard");
  console.log("Delete sandbox:    pnpm run test:db:destroy");
